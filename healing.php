			<p>I have been concerned about the devastating practice of mountain top   removal coal mining for quite some time. The  feeling of  wishing I   could do something to help stop it gnawed at me.  Eric Reece, author of   Lost Mountain, wrote a compelling article about MTR for Orion magazine.     What was so powerful about the article is that Eric connected the   reader to the people of Appalachia, and their stories of floods,   poisoned water, personal illness, and death.  Then I realized what I   needed to do; we need to connect our communities to educate people on   this issue, in order to put an end to mountain top removal, and so the   healing blankets for the earth project was born.  	What is a healing blanket?  It is created with wool fleece, and   gathered organic materials,  seeds from flowers, weeds, acorns,   grasses, shells,etc. and felted into burlap. The idea is to connect   students to their environment, and learn about the Appalachian   Mountains and mountain top removal coal mining at the same time.    Talking about environmental issues becomes more meaningful to people   when they are engaged in a process that brings them into the woods, and   fields, or offers them an opportunity to investigate natural materials   close up. The fleece comes in many colors and it becomes the pallet for   the blanket.  The students design the blanket combining their ideas.    The blankets are about the size of a twin bed. When they are finished,   they are mailed to people whose stories have touched us.  It is then   installed by the recipient into the landscape to help regenerate and   heal the ecosystem that has been damaged by MTR.  This eco-art   installation  acts as a bridge between our communities, and offers   support to the people in coal country. </p>
