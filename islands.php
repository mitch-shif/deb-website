			<p>I have been working as a landscape - garden designer for more than 
twenty years.   Plants and flowers are used in my designs for  gardens, 
as an artist would use paint  from his pallet applied to a canvas.   
Observing the changes in my community over the years, has caused me to 
redefine the functions of a garden, and  public parks.  Our town not so 
long ago had many farms, and now it has many streets with lots of 
houses.  The field next to my house used to be  lit up on warm summer 
nights by hundreds of fire flies, now there is a built in pool.  Where 
will the fire flies go?  What has happened to them?</p>
            <p> Sprawl and development is eating up our open spaces, and because of 
              this I believe we need to look for opportunities in our communities,  
              to  recreate habitats for wildlife.  These spaces can be part of 
              municipal parks, in school yards, landfills, deserted city lots, and 
              your own backyard.  These islands are also places where people can 
              connect with nature, and their surroundings.</p>
            <p> Stewart Park in Exeter looks like any other park with benches for 
              people to sit on to read, or  enjoy the beautiful view of the river.  
              It is a tranquil place located in the downtown area, and is an island 
              of habitat.  The center piece of the garden is a large tree trunk that  
              looks like a whale, it was pulled from the river after a flood.  This 
              driftwood connects the park to the  tidal river and  the ocean, and the 
              history of the New England town.  It is set on a bed of gravel and 
              stones, which help filter storm run off water from the abutting parking 
              lot.  There is a bed of grasses which serve as nesting material for 
              birds.  The bushes and trees serve as nesting sites and feeding 
              stations.  They all bloom with lovely flowers, that turn into fruit for 
              the animals and birds.  Even the flowers that are planted in the beds 
              make seeds for food or nectar for humming birds and bees. The plants 
              are hardy drought resistant plants that require very little care.              </p>
            <p>The Powder House Park was planted with a group of students from a 
              local public school.  In this workshop students researched the Wood 
              Thrush, and helped create and plant the garden.   This bird is on the 
              Audubon watch list, because of its severely declining population, due 
              to lack of habitat. Plants in this garden includes, blueberry bushes, 
              viburnum, spice bush and goldstrum, to name a few.</p>
